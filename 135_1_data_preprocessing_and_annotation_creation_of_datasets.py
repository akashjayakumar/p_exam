import pandas as pd
from sklearn.model_selection import train_test_split

# Create a synthetic dataset
data = {
    'text': [
        'I really enjoyed the movie!',
        'The weather is terrible today.',
        'The product is excellent.',
        'I dislike the service.',
        'This is a fantastic experience.',
        'I have complaints about the quality.'
    ],
    'label': [1, 0, 1, 0, 1, 0]
}

df = pd.DataFrame(data)

# Display the original dataset
print("Original Dataset:")
print(df)

# Optional: Data Preprocessing
df['text'] = df['text'].str.lower()  # Convert text to lowercase

# Split the dataset into training and testing sets
train_data, test_data = train_test_split(df, test_size=0.2, random_state=42)

# Annotation
def annotate_data(row):
    print(f"Text: {row['text']}")
    label = input("Enter label (0 or 1): ")
    return int(label)

# Apply annotation to the training set
train_data['label'] = train_data.apply(annotate_data, axis=1)

# Display the annotated training dataset
print("\nAnnotated Training Dataset:")
print(train_data)

# Save the annotated dataset
train_data.to_csv('annotated_dataset.csv', index=False)